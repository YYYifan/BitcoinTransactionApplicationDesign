    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int row = BankAccountTable.getSelectedRow();
        if(row<0){
            JOptionPane.showMessageDialog(null, "Please select a row!!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        int an = (Integer) BankAccountTable.getValueAt(row, 0);
        BankAccount bankAccount = new BankAccount();
        for (BankAccount ba : customer.getBankAccountList()) {
            if (ba.getAccountNumber() == an) {
                customer.getBankAccountList().remove(ba);
                bankAccount = ba;
                break;
            }
        }
        Bank bank = bankAccount.getBank();
        bank.getBankAccountList().remove(bankAccount);
        business.getBankAccountDirectory().getBankAccountDirectory().remove(bankAccount);
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int row = BankAccountTable.getSelectedRow();
        if(row<0){
            JOptionPane.showMessageDialog(null, "Please select a row!!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        int an = (Integer) BankAccountTable.getValueAt(row, 0);
        for (BankAccount ba : customer.getBankAccountList()) {
            if (ba.getAccountNumber() == an) {
                baa = ba;
                break;
            }
        }
        bnTf.setText(baa.getBankName());
        anTf.setText(String.valueOf(baa.getAccountNumber()));
        bTf.setText(String.valueOf(baa.getBalance()));
        populateTransactionRecords();
    }//GEN-LAST:event_jButton1ActionPerformed
    public void populateTransactionRecords() {
        DefaultTableModel model = (DefaultTableModel) trTable.getModel();
        
        model.setRowCount(0);
        
        for (BankTransactionRecord btr : baa.getTransactionRecordDirectory().getFundsTransactionDirectory()){
            Object[] row = new Object[8];
            row[0] = btr.getDate();
            row[1] = btr.getSenderBank();
            row[2] = btr.getSenderAccountNumber();
            row[3] = btr.getSenderName();
            row[4] = btr.getReceiverBank();
            row[5] = btr.getReceiverAccountNumber();
            row[6] = btr.getReceiverName();
            row[7] = btr.getFee();
            model.addRow(row);
        }
    }

