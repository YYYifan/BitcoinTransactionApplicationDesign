    public void populateBankAccountTable() {
        DefaultTableModel model = (DefaultTableModel) BankAccountTable.getModel();
        
        model.setRowCount(0);
        
        for (BankAccount ba : customer.getBankAccountList()){
            Object[] row = new Object[4];
            row[0] = ba.getAccountNumber();
            row[1] = ba.getBankName();
            row[2] = ba.getDate();
            row[3] = ba.getBalance();
            model.addRow(row);
        }
    }

