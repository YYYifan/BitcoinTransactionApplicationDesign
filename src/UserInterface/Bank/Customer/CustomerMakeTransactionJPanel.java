    public void populateCombo1() {
        bnCombo1.removeAllItems();

        ArrayList<String> bankname = new ArrayList<String>();
        for (BankAccount ba : customer.getBankAccountList()) {
            if (bankname.isEmpty()) {
                bankname.add(ba.getBankName());
            } else {
                for (int i = 0; i < bankname.size(); i++) {
                    if (ba.getBankName().equals(bankname.get(i)))
                        break;
                    bankname.add(ba.getBankName());
                }
            }
        }
        for (String ss : bankname)
            bnCombo1.addItem(ss);
        
    }
    public void populteCombo2() {
        anCombo1.removeAllItems();
        String bankname = (String) bnCombo1.getSelectedItem();
        for (BankAccount ba : customer.getBankAccountList()) {
            if (ba.getBankName().equals(bankname))
                anCombo1.addItem(String.valueOf(ba.getAccountNumber()));
        }
    }
