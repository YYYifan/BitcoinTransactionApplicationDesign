    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String password = pwTf.getText();
        int amount = Integer.parseInt(amountTf.getText());
        if (baa.getPassword().equals(password)) {
            if (baa.getBalance() < amount) {
                JOptionPane.showMessageDialog(null, "Your balance is not enough!");
            } else {
                String bankname = bnTf.getText();
                String receiver = rnTf.getText();
                int an = Integer.parseInt(anTf.getText());
                BankAccount rBa = business.getBankAccountDirectory().searchBankAccount(an);
                if (receiver.equals(rBa.getCustomerName())) {
                    BankTransactionRecord left = new BankTransactionRecord();
                    BankTransactionRecord right = new BankTransactionRecord();
                    left.setAccountNumber(baa.getAccountNumber());
                    left.setCustomer(baa.getCustomerName());
                    left.setDate(new Date());
                    left.setDirection("Out");
                    left.setFee(amount);
                    left.setReceiverAccountNumber(rBa.getAccountNumber());
                    left.setReceiverBank(rBa.getBankName());
                    left.setReceiverName(receiver);
                    left.setSenderAccountNumber(baa.getAccountNumber());
                    left.setSenderBank(baa.getBankName());
                    left.setSenderName(baa.getCustomerName());
                    baa.getTransactionRecordDirectory().getFundsTransactionDirectory().add(left);
                    baa.setBalance(baa.getBalance() - amount);
                    right.setAccountNumber(rBa.getAccountNumber());
                    right.setCustomer(rBa.getCustomerName());
                    right.setDate(new Date());
                    right.setDirection("In");
                    right.setFee(amount);
                    right.setReceiverAccountNumber(rBa.getAccountNumber());
                    right.setReceiverBank(rBa.getBankName());
                    right.setReceiverName(receiver);
                    right.setSenderAccountNumber(baa.getAccountNumber());
                    right.setSenderBank(baa.getBankName());
                    right.setSenderName(baa.getCustomerName());
                    rBa.getTransactionRecordDirectory().getFundsTransactionDirectory().add(right);
                    rBa.setBalance(rBa.getBalance()+amount);
                } else {
                    JOptionPane.showMessageDialog(null, "The Bank Account is not macthed with the receiver you input!");
                }
            }
                
        } else {
            JOptionPane.showMessageDialog(null, "Invalid Password!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed


