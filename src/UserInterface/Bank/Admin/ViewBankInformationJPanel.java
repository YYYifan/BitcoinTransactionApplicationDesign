    private void addJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addJButtonActionPerformed
       int row = BankAccountTable.getSelectedRow();
        if(row<0){
            JOptionPane.showMessageDialog(null, "Please select a row!!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        BankAccount bankaccount = (BankAccount) BankAccountTable.getValueAt(row, 1);
        business.getBankAccountDirectory().removeBankAccount(bankaccount.getAccountNumber());
        for (Bank b : business.getBankDirectory().getBankDirectory()) {
            if (b.getId() == bank.getId()) {
                b.getBankAccountList().remove(bankaccount);
            }
        }

        populateAccountTable();

    }//GEN-LAST:event_addJButtonActionPerformed

