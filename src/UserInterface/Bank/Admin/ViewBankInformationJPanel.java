    public void populateAccountTable(){
        DefaultTableModel model = (DefaultTableModel) BankAccountTable.getModel();
        
        model.setRowCount(0);
        
        for (BankAccount bankaccount : bank.getBankAccountList()){
            Object[] row = new Object[4];
            row[0] = bankaccount.getAccountNumber();
            row[1] = bankaccount;
            row[2] = bankaccount.getDate();
            row[3] = bankaccount.getBalance();
            
            model.addRow(row);
        }
    }

