    private void populateTable(){
        DefaultTableModel model = (DefaultTableModel) BankTable.getModel();
        
        model.setRowCount(0);
        
        for (Bank bank : business.getBankDirectory().getBankDirectory()){
            Object[] row = new Object[2];
            row[0] = bank.getId();
            row[1] = bank;
            
            model.addRow(row);
        }
    }
