    private void addJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addJButtonActionPerformed

//        Type type = (Type) organizationJComboBox.getSelectedItem();
//        directory.createOrganization(type);
//        populateTable();
        int row = BankTable.getSelectedRow();
        if(row<0){
            JOptionPane.showMessageDialog(null, "Please select a row!!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Bank bank = (Bank) BankTable.getValueAt(row, 1);
        ViewBankInformationJPanel manageOrganizationJPanel = new ViewBankInformationJPanel(userProcessContainer, business, bank);
        userProcessContainer.add("ViewBankInformationJPanel", manageOrganizationJPanel);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
    }//GEN-LAST:event_addJButtonActionPerformed

