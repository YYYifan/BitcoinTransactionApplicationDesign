    private void jButtonSelectSellingActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSelectSellingActionPerformed
        // TODO add your handling code here:
        jButtonSelectSelling.setEnabled(false);
        int row = jTableSelling.getSelectedRow();
        if(row<0){
            JOptionPane.showMessageDialog(null, "Please select a row!!", "Warning", JOptionPane.WARNING_MESSAGE);
            jButtonSelectSelling.setEnabled(true);
            return;
        }
        DateFormat df = new SimpleDateFormat("MM/dd/yyyy HH:mm:ss");
        Date datesell = (Date) jTableSelling.getValueAt(row, 1);
        String date1 = df.format(datesell);
        Customer investorSell = (Customer) jTableSelling.getValueAt(row, 0);
        int sellingQuantity = (int) jTableSelling.getValueAt(row, 3);
        jTextFieldSellerName.setText(investorSell.getName());
        jTextFieldSellingQuantity.setText(String.valueOf(sellingQuantity));
        jTextFieldSellingDate.setText(date1);
    }//GEN-LAST:event_jButtonSelectSellingActionPerformed

