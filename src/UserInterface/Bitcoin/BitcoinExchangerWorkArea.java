    private void jButtonMatchingBuyerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonMatchingBuyerActionPerformed
        // TODO add your handling code here:
        jButtonMatchingBuyer.setEnabled(false);
        int j = Integer.parseInt(jTextFieldSellingQuantity.getText());
        for(InvestorWorkRequest iwr : business.getExchangerWorkQueue().getWorkRequestList()){
            if(iwr.getMessage().equals("Buying.")){
                if(iwr.getQuantity()<=j){
                    for(int i = 0; i <= j; i++){
                        if(iwr.getQuantity() + i == j){
                            DateFormat df = new SimpleDateFormat("MM/dd/yyyy HH:mm:ss");
                            Date datebuy = (Date)iwr.getRequestDate();
                            String date1 = df.format(datebuy);
                            jTextFieldBuyerName.setText(iwr.getSender().getName());
                            jTextFieldBuyingQuantity.setText(String.valueOf(iwr.getQuantity()));
                            jTextFieldBuyingDate.setText(date1);
                        }break;
                    }
                }
            }
        }
        
    }//GEN-LAST:event_jButtonMatchingBuyerActionPerformed

