    private void populateSellingTable() {
        DefaultTableModel model = (DefaultTableModel) jTableSelling.getModel();
        model.setRowCount(0);
        for(InvestorWorkRequest iwr : business.getExchangerWorkQueue().getWorkRequestList()){
            if(iwr.getMessage().equals("Selling.")){
                Object[] row = new Object[5];
                row[0] = iwr.getSender();
                row[1] = iwr.getRequestDate();
                row[2] = iwr.getStatus();
                row[3] = iwr.getQuantity();
                row[4] = iwr.getMessage();
                model.addRow(row);
            }
        }
    }
    
